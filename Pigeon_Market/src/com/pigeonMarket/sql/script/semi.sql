-- 상품구입 뷰테이블
CREATE OR REPLACE VIEW PRODUCT_SALE
as
SELECT PRODUCT_OK_NO, pok.P_ID, PRICE, CATEGORY_CODE, PRODUCT_TITLE, PRODUCT_CONTENTS, REG_DATE, APPROVAL_DATE, PRODUCT_STATE, STATUS
FROM PRODUCT_OK pok
JOIN PRODUCT USING(PRODUCT_NO);


-- 상품구입 사진 뷰테이블
CREATE OR REPLACE VIEW ATTACHMENT_SALE
as
SELECT F_ID, PRODUCT_OK_NO, ORIGIN_NAME, CHANGE_NAME, FILE_PATH, FILE_LEVEL
FROM ATTACHMENT
JOIN PRODUCT_OK USING(PRODUCT_NO);

