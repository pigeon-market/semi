-- 블랙리스트 추가시 회원테이블 상태 변경
CREATE OR REPLACE TRIGGER BLACK_CODE_UPDATE
AFTER INSERT
ON BLACK_LIST
FOR EACH ROW
BEGIN
    UPDATE MEMBER
    SET BLACK_CODE = 'Y'
    WHERE USER_ID = :NEW.BLACK_ID;
END;
/

-- 블랙리스트 추가시 상품테이블 승인상태 N로 변경
CREATE OR REPLACE TRIGGER BLACK_CODE_UPDATE2
AFTER INSERT
ON BLACK_LIST
FOR EACH ROW
BEGIN
    UPDATE Product
    SET APPROVAL = 'N'
    WHERE P_ID = :NEW.BLACK_ID;
    
END;
/

